    private void actualizarTable1(){
        
        String [] encabezado = new String[3];
        encabezado[0]="C贸digo";
        encabezado[1]="Nombre";
        encabezado[2]="Contacto";

        
        
        var datos = new Object[this.facturaControl.listar().size()][3];
        
        var i=0;
        for(var factura:this.facturaControl .listar())
        {
            datos[i][0]=factura.getCodigo();
            datos[i][1]=factura.getNombre();
            datos[i][2]=factura.getDireccion();
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
        
    }
