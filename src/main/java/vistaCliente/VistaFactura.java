    private void actualizarTabla()
    {
        String [] encabezado = new String[3];
        encabezado[0]="CÃ³digo";
        encabezado[1]="Nombre";
        encabezado[2]="Contacto";
        
        var datos = new Object[this.facturaControl.listar().size()][3];
        
        var i=0;
        for(var capitan:this.facturaControl.listar())
        {
            datos[i][0]=capitan.getCodigo();
            datos[i][1]=capitan.getNombre();
            datos[i][2]=capitan.getDireccion();
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
    }
    
